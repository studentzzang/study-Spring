public class User {
    String id;
    String name;
    String pw;

    public String getId() {
        return id;
    }

    public String getName(){
        return name;
    }

    public String getPw(){
        return pw;
    }

    public void SetId(String id){
        this.id = id;
    }

    public void SetName(String name){
        this.name = name;
    }

    public void SetPw(String pw){
        this.pw = pw;
    }
}
